
def main():

    purchase = []
    all_p = 0
    days = ["понедельник","вторник","среда","четверг","пятница","суббота","воскресенье"]
    for x in days:
        s = int(input("Введите продажи за {}:" .format(x)))
        purchase.append(s)
    print(purchase)

    for z in purchase:
        all_p += z

    print(all_p)
        


        
if __name__ == "__main__":
  main()


                            

                                

                               
        
    

    
     


        

  

    









    
    
    
    
       

 


 


            
        

    
     
    
    
   

     
   
    
   
                       


    





    


    

    


          
        
    





        












              







    
    

 







    

    

