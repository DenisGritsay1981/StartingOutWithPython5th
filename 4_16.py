


import turtle

n = 20
turtle.hideturtle()
turtle.setup (1000,1000)
turtle.setheading(90)

for x in range(100):
    turtle.forward(n+x*2)
    turtle.setheading(180)
    turtle.forward(n+x*2)
    turtle.setheading(270)
    turtle.forward(n+x*2)
    turtle.setheading(0)
    turtle.forward(n+x*2)
    turtle.setheading(90)


    
       
    

    

    
        



    
    











              







    
    

 







    

    

