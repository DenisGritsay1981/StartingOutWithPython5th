
def main():

    q = "Д"
    x = 0

    while q.upper() == "Д":
        print("Введите число")
        z = int(input())
        x += z
        print("Если еще напишите Д")
        q = input()
        
    print(f"сумма чисел равна {x}")
                          
if __name__ == "__main__":
  main()


                            
 
                                

                               
        
    

    
     


        

  

    









    
    
    
    
       

 


 


            
        

    
     
    
    
   

     
   
    
   
                       


    





    


    

    


          
        
    





        












              







    
    

 







    

    

