

def kinetic_energy(m,v):
    Ke = 1/2*m*v
    return Ke

m = float (input("Введите массу тела:   "))
v = float (input("Введите скорость тела:   "))

print(f"Кинетическая энергия тела равна {kinetic_energy(m,v)}")
    
    
    


    
    
    
    


    
    




    

    
 

    






    

    

 

    


    
    









    
 
    

    

    



    
   
