
def main():

    line = []

    z = "д"

    while z == "д":
        
        q = int(input("Введите число в список:   "))
        line.append(q)
        z = input("Введите д если хотите продолжить:   ")


    n = int(input("Введите число, больше которых, хотите найти в списке:  "))

    for x in line:
        if x > n:
            print(f"{x}")
            

    

    print(line)
        

    

        


    

                
if __name__ == "__main__":
  main()


                            

                                

                               
        
    

    
     


        

  

    









    
    
    
    
       

 


 


            
        

    
     
    
    
   

     
   
    
   
                       


    





    


    

    


          
        
    





        












              







    
    

 







    

    

