def main():
    
    
    y = int(input("Введите целое число: "))
    x = int(input("Введите степень числа: "))
    result = m(y,x)
    print(result)
    
def m(y,x):
    if x == 1:
        return y
    elif x>1:
        return y * m(y,x-1)
    
